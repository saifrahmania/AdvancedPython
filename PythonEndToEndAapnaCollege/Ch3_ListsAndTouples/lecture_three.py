# List is mutable
# List is defined by square brackets []
# List can have any data type
# List can have any number of items
# List can have duplicate items
# List can have nested lists
# List can have different data types
# List can be empty
# List can be accessed using index
# List can be sliced
# List can be updated
# List can be deleted
# List can be sorted
# List can be reversed
# List can be concatenated
# List can be repeated
# List can be iterated
# List can be unpacked
# List can be copied
# List can be joined
marks = [94.4, 87.5, 90.0, 78.9, 85.0]
