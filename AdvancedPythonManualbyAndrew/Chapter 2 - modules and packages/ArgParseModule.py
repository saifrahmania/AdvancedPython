# The Argparse Module:
# The argparse module makes it easy to write user-friendly command-line interfaces.
# The program defines what arguments it requires, and argparse will figure out how to parse those out of sys.argv.
# The argparse module also automatically generates help and usage messages and issues errors when users give the program invalid arguments.
# The argparse module is part of the standard library.
# The argparse module requires Python 2.7 or later.
# The argparse module is an alternative to the older optparse module.
# The argparse module is a replacement for optparse.
# The argparse module is a replacement for getopts.
